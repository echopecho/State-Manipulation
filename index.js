function setFailing() {
  return {
    ...state,
    students: state.students.map(student => {
      const gradeTotal = student.classes
        .map(({ grade }) => grade)
        .reduce((acc, cur) => acc + cur);

      if (gradeTotal / 5 > 70) {
        return { ...student };
      } else {
        return { ...student, passing: false };
      }
    })
  };
}

